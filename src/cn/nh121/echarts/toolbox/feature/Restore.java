package cn.nh121.echarts.toolbox.feature;

public class Restore
{
    private Boolean show;
    private String  title;

    /**
     * @return the show
     */
    public Boolean getShow()
    {
        return show;
    }

    /**
     * @param show
     *            the show to set
     */
    public Restore setShow(Boolean show)
    {
        this.show = show;
        return this;
    }

    /**
     * @return the title
     */
    public String getTitle()
    {
        return title;
    }

    /**
     * @param title
     *            the title to set
     */
    public Restore setTitle(String title)
    {
        this.title = title;
        return this;
    }
}
